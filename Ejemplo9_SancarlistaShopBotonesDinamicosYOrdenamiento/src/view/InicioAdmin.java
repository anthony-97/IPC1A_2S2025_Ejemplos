    public void llenarTablaVendedores(Vendedor[] vendedores, int contador) {
        DefaultTableModel modelo = (DefaultTableModel) this.tablaVendedores.getModel();
        modelo.setRowCount(0); //Limpieza de la tabla
        for (int i = 0; i<contador; i++) {
            String[] fila = new String[]{
                vendedores[i].getCodigo(),
                vendedores[i].getNombre(),
                vendedores[i].getGenero(),
                Integer.toString(vendedores[i].getVentasConfirmadas())
            };
            modelo.addRow(fila);
        }
    }
    
    public void llenarTablaProductos(Producto[] productos, int contador) {
        DefaultTableModel modelo = new DefaultTableModel(
            new Object[]{"Código", "Nombre", "Categoría", "Detalle"}, 0
        ) {
            @Override
            public boolean isCellEditable(int row, int column) {
                //Solo la columna de acciones es editable (para que funcione el botón)
                return column == 3;
            }
        };

        tablaProductos.setModel(modelo);

        for (int i = 0; i < contador; i++) {
            Producto producto = productos[i];
            modelo.addRow(new Object[]{
                producto.getCodigo(),
                producto.getNombre(),
                producto.getCategoria(),
                "" //Valor por defecto para rellenar la celda
            });
        }

        //Se crea un listener para el boton de "Ver Detalle"
        ActionListener listener = new ActionListener() {
            @Override
            public void actionPerformed(ActionEvent e) {
                int fila = tablaProductos.getSelectedRow();
                if (fila >= 0) {
                    String codigo = (String) tablaProductos.getValueAt(fila, 0);
                    InicioAdmin ventanaAdmin = InicioAdmin.this; //Recuperando la vista de administracion dentro del listener
                    ventanaAdmin.getControlador().mostrarVentana(TipoVentana.Ventana.VENTANA_PRODUCTO);
                    ventanaAdmin.getControlador().getControladorVentanas().llenarVentanaProducto(codigo);
                }
            }
        };

        //Se genera la columna de Detalles
        TableColumn columnaDetalle = tablaProductos.getColumn("Detalle");
        ButtonEditorRenderer buttonRenderer = new ButtonEditorRenderer(listener, "Ver Detalle");
        columnaDetalle.setCellRenderer(buttonRenderer);
        columnaDetalle.setCellEditor(buttonRenderer);
        tablaProductos.setRowHeight(40);
    }
    
