    class ButtonEditorRenderer extends AbstractCellEditor implements TableCellRenderer, TableCellEditor {
        private JPanel panel;
        private JButton boton;

        public ButtonEditorRenderer(ActionListener listener, String texto) {
            panel = new JPanel(new FlowLayout(FlowLayout.CENTER));
            boton = new JButton(texto);
            boton.addActionListener(e -> listener.actionPerformed(e));
            panel.add(boton);
        }

        @Override
        public Component getTableCellRendererComponent(JTable table, Object value, boolean isSelected, boolean hasFocus, int row, int column) {
            return panel;
        }

        @Override
        public Component getTableCellEditorComponent(JTable table, Object value, boolean isSelected, int row, int column) {
            return panel;
        }

        @Override
        public Object getCellEditorValue() {
            return null;
        }
    }
