    public void llenarTablaVendedores(Vendedor[] vendedores, int contador) {
        DefaultTableModel modelo = (DefaultTableModel) this.tablaVendedores.getModel();
        modelo.setRowCount(0); //Limpieza de la tabla
        for (int i = 0; i<contador; i++) {
            String[] fila = new String[]{
                vendedores[i].getCodigo(),
                vendedores[i].getNombre(),
                vendedores[i].getGenero(),
                Integer.toString(vendedores[i].getVentasConfirmadas())
            };
            modelo.addRow(fila);
        }
        
    }
    
