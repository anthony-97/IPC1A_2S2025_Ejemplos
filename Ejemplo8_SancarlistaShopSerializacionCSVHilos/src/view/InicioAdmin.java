    public String cargarCSV() {
        JFileChooser sel = new JFileChooser();
        sel.setDialogTitle("Seleccionar archivo CSV");
        sel.setFileFilter(new FileNameExtensionFilter("Archivos CSV (*.csv)", "csv"));

        int resultado = sel.showOpenDialog(null);
        if (resultado == JFileChooser.APPROVE_OPTION) {
            File archivo = sel.getSelectedFile();
            if (archivo != null) {
                StringBuilder contenido = new StringBuilder();
                try (BufferedReader br = new BufferedReader(new FileReader(archivo))) {
                    String linea;
                    while ((linea = br.readLine()) != null) {
                        contenido.append(linea).append("\n");
                    }
                } catch (IOException e) {
                    this.mostrarError("Error al leer archivo");
                    return null;
                }
                return contenido.toString(); //Retorna el contenido como String
            }
        } else {
            this.mostrarError("No se seleccionó ningún archivo");
        }
        return null;
    }
    
